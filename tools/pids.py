def extract(data):
    if data[:3] == b'\xff\xff\xff':
        pid_char = data[3]
        pid = 768 + pid_char
        start = 4
    elif data[:2] == b'\xff\xff':
        pid_char = data[2]
        pid = 512 + pid_char
        start = 3
    elif data[:1] == b'\xff':
        pid_char = data[1]
        pid = 256 + pid_char
        start = 2
    else:
        pid_char = data[0]
        pid = pid_char
        start = 1

    if pid_char in range(0, 128):
        data_len = 1
        end = start + data_len
    elif pid_char in range(128, 192):
        data_len = 2
        end = start + data_len
    elif pid_char == 254:
        # The rest of the message is the data
        end = -1
        data_len = len(data[start:])
    else:
        # Variable length, the first data char is the length
        data_len = data[start]
        start += 1
        end = start + data_len

    val_bytes = data[start:end]
    if 'resolution' in pid_data[pid] and \
            isinstance(pid_data[pid]['resolution'], Fraction) and \
            val_bytes != b'\xff' * data_len:
        # First byte swap the data (it's in little endian) and then multiply by 
        # the resolution
        res = pid_data[pid]['resolution']
        le_val = int.from_bytes(val_bytes, 'little')
        fractional_val = le_val * res

        if 'units' in pid_data[pid]:
            value = f'{float(fractional_val)} {pid_data[pid]["units"]} ({val_bytes.hex()})'
        else:
            value = f'{float(fractional_val):f} ({val_bytes.hex()})'
    else:
        value = val_bytes.hex()

    # Return a dictionary representing the PID and then the rest of the message
    obj = {
        'pid': pid,
        'name': params[pid],
        'data': value,
        'raw': data[:end],
    }
    rest = data[end:]

    return (obj, rest)
